#Conceptos Basicos:
'''Que es un fichero?
Un conjunto de bits almacenados en un dispositivo de memoria persistente,
identificado con un nombre, una direccion a la ruta que lo contiene y una extension

Que operaciones permite?
Creacion - Apertura - Cierre - Extencion

Que es el puntero del fichero?
Representa el lugar donde apunta el fichero antes de realizar una operacion
determinada, seria como el lugar donde ponemos nuestro dedo o fijamos la vista
mientras leemos o escribimos

Ficheros de textos y ficheros binarios:
Los ficheros de texto guardan datos en forma de texto plano, son entendivles por
las personas, pero los binarios no, por que los guarda en el lenguaje de la maquina
'''